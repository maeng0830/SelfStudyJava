package sec04.verify.exam04;

public class Printer {
	void println(int input) {
		System.out.println(input);
	}
	
	void println(boolean input) {
		System.out.println(input);
	}
	
	void println(double input) {
		System.out.println(input);
	}
	
	void println(String input) {
		System.out.println(input);
	}

}
